package Entidades;

public class NacionalidadEntity {

    private String CodNac = "";
    private String Pais_Nac = "";
    private String Gentilicio_Nac = "";

    /**
     * @return the CodNac
     */
    public String getCodNac() {
        return CodNac;
    }

    /**
     * @param CodNac the CodNac to set
     */
    public void setCodNac(String CodNac) {
        this.CodNac = CodNac;
    }

    /**
     * @return the Pais_Nac
     */
    public String getPais_Nac() {
        return Pais_Nac;
    }

    /**
     * @param Pais_Nac the Pais_Nac to set
     */
    public void setPais_Nac(String Pais_Nac) {
        this.Pais_Nac = Pais_Nac;
    }

    /**
     * @return the Gentilicio_Nac
     */
    public String getGentilicio_Nac() {
        return Gentilicio_Nac;
    }

    /**
     * @param Gentilicio_Nac the Gentilicio_Nac to set
     */
    public void setGentilicio_Nac(String Gentilicio_Nac) {
        this.Gentilicio_Nac = Gentilicio_Nac;
    }

}
